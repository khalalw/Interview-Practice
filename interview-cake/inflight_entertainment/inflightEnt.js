const canTwoMoviesFillFlight = (movieLengths, flightLength) => {
  const prevLengths = new Set();

  for (let i = 0; i < movieLengths.length; i += 1) {
    const curr = movieLengths[i];
    const target = flightLength - curr;

    if (prevLengths.has(target)) {
      return true;
    }

    prevLengths.add(curr);
  }

  return false;
};

// tests
const assertEquals = (a, b, desc) => {
  if (a === b) {
    console.log(`${desc} ... PASS`);
  } else {
    console.log(`${desc} ... FAIL: ${a} != ${b}`);
  }
};

// @ts-ignore
let desc = 'short flight';
let actual = canTwoMoviesFillFlight([2, 4], 1);
let expected = false;
assertEquals(actual, expected, desc);

desc = 'long flight';
actual = canTwoMoviesFillFlight([2, 4], 6);
expected = true;
assertEquals(actual, expected, desc);

desc = 'one movie half flight length';
actual = canTwoMoviesFillFlight([3, 8], 6);
